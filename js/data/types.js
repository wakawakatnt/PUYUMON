/**
 * タイプ相性計算
 * @param {string} atkType 攻撃タイプ
 * @param {string[]} defTypes 防御タイプ（1〜2つ）
 * @returns {number} 最終倍率
 */
function getTypeEffectiveness(atkType, defTypes) {
  let mult = 1;
  defTypes.forEach(def => {
    mult *= (TYPE_CHART[atkType]?.[def] ?? 1);
  });
  return mult;
}

/**
 * 相性メッセージ取得
 */
function getEffectivenessMessage(mult) {
  if (mult === 0) return '効果がないようだ…';
  if (mult >= 4) return 'こうかばつぐん！（×4）';
  if (mult >= 2) return 'こうかばつぐん！';
  if (mult < 1 && mult > 0) return '効果はいまひとつのようだ…';
  return '';
}

